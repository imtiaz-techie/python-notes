a = 1
print(a)
print(type(a))

b = True
print(b)
print(type(b))

c = "Minar"
print(c)
print(type(c))

d = 1.5
print(d)
print(type(d))

e = complex(3,4) #3+4j
print(e)
print(type(e))

list1 = [8, 2.3, [-4, 5], ["mango", "banana"]] # collection of data with elements separated by a comma and enclosed within square brackets.
#Lists are mutable and can be modified after creation.
print(list1)

tuple1 = (("parrot", "sparrow"), ("Lion", "Tiger")) #collection of data with elements separated by a comma and enclosed within parentheses. 
#Tuples are immutable and can not be modified after creation.
print(tuple1)

dict1 = {"name":"Sakshi", "age":20, "canVote":True} #unordered collection of data containing a key:value pair. 
#The key:value pairs are enclosed within curly brackets.
print(dict1)